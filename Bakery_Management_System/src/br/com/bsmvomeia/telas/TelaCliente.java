    private void btn_addClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addClienteActionPerformed
        if (txtidCliente.getText().isEmpty()) {
            Cliente c = new Cliente(TelaCliente.txtnomeCliente, TelaCliente.txtfoneCliente, TelaCliente.txtenderecoCliente, TelaCliente.txtemailCliente, TelaCliente.txtidCliente, TelaCliente.txtDataCliente);
            int confere = c.adicionarCliente();
            if (confere > 0) {
                this.dispose();
                TelaPesquisaCliente.telaClienteflag = 0;
            }

        } else {
            Cliente c = new Cliente(TelaCliente.txtnomeCliente, TelaCliente.txtfoneCliente, TelaCliente.txtenderecoCliente, TelaCliente.txtemailCliente, TelaCliente.txtidCliente, TelaCliente.txtDataCliente);
            int confere = c.alterarCliente(tabelaPesquisa);
            if (confere > 0) {
                this.dispose();
                TelaPesquisaCliente.telaClienteflag = 0;
            }

        }

        // TODO add your handling code here:
    }//GEN-LAST:event_btn_addClienteActionPerformed

