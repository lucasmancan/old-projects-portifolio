    private void btn_edit_ProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_edit_ProdutoActionPerformed
        if (telaClienteflag == 1) {
            JOptionPane.showMessageDialog(null, "Realize uma operaÃ§Ã£o por vez");
        } else {
            if (tabelaPesquisa.getSelectedRow() == -1) {
                JOptionPane.showMessageDialog(null, "Selecione um produto da tabela para poder edita-lo");

            } else {
                TelaCliente cli = new TelaCliente();
                Cliente c = new Cliente(TelaCliente.txtnomeCliente, TelaCliente.txtfoneCliente, TelaCliente.txtenderecoCliente, TelaCliente.txtemailCliente, TelaCliente.txtidCliente, TelaCliente.txtDataCliente);
                cli.setVisible(true);
                c.setarCamposCliente(tabelaPesquisa);

            }
        }
    }//GEN-LAST:event_btn_edit_ProdutoActionPerformed

    private void btn_del_ProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_del_ProdutoActionPerformed
        if (telaClienteflag == 1) {
            JOptionPane.showMessageDialog(null, "Realize uma operaÃ§Ã£o por vez");
        } else {
            if (tabelaPesquisa.getSelectedRow() == -1) {
                JOptionPane.showMessageDialog(null, "Selecione um cliente da tabela para poder remove-lo");

            } else {
                Cliente c = new Cliente(TelaCliente.txtnomeCliente, TelaCliente.txtfoneCliente, TelaCliente.txtenderecoCliente, TelaCliente.txtemailCliente, TelaCliente.txtidCliente, TelaCliente.txtDataCliente);
                c.removerCliente(tabelaPesquisa);
                c.atualizarTabelaClientes();

            }
        }   // TODO add your handling code here:
    }//GEN-LAST:event_btn_del_ProdutoActionPerformed

